const LANG_KEY = "aimos.lang";

export function getLang() {
  const stored = localStorage.getItem(LANG_KEY);
  if (stored && STRINGS[stored]) return stored;
  const nav = (typeof navigator !== "undefined" && navigator.language) || "en-GB";
  if (nav.startsWith("sv")) return "sv";
  if (nav.toLowerCase() === "en-us") return "en-US";
  return "en-GB";
}

export function setLang(code) {
  if (STRINGS[code]) localStorage.setItem(LANG_KEY, code);
}

export function makeT(lang) {
  const dict = STRINGS[lang] || STRINGS["en-GB"];
  const fallback = STRINGS["en-GB"];
  return (key) => dict[key] ?? fallback[key] ?? key;
}

export function aiLanguageDirective(lang) {
  if (lang === "sv") {
    return "Respond entirely in Swedish (svenska). Use natural Swedish HR/business language and Swedish idioms. Keep JSON keys in English.";
  }
  if (lang === "en-US") {
    return "Respond in American English. Use US spelling (e.g. organize, behavior, prioritize) and US idioms.";
  }
  if (lang === "en-AU") {
    return "Respond in Australian English. Use UK spelling conventions (organise, behaviour, prioritise) but feel free to use natural Australian phrasing where it reads well in a formal HR context.";
  }
  return "Respond in British English. Use UK spelling (e.g. organise, behaviour, prioritise) and UK idioms.";
}
